# Matematicos

# suma
print(3+4)  # 7
# resta
print(12-4)  # 8
# multitplicacion
print(10*4)  # 40
# division
print(12/4)  # 3
# potencia
print(2**4)  # base es 2 y el exponente es 4 - 16
# division entera
print(12//5)  # 2
# operador modulo - resto de la division
print(12 % 5)  # 2

print("*" * 100)
# operadores de incremento y decremento
numero = 2  # operacion de asignacion lo que hay en la derecha se guarda en la izquierda
numero += 3  # numero = numero + 3
print(numero)  # 5
numero -= 2
print(numero)  # 3
