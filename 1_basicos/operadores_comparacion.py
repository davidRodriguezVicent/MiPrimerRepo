numero1 = 16
numero2 = 6

# operaciones de comparacion devuelven siempre True o False

# Mayor o mayor o igual
resultado = numero1 > numero2  # >=
print('mayor que: ', resultado)  # True


# Menor o menor o igual
resultado = numero1 < numero2  # <=
print('menor que: ', resultado)  # False

# Igual
resultado = numero1 == numero2  # ==
print('igual que: ', resultado)  # False

# Distinto
resultado = numero1 != numero2  # !=
print('distinto que: ', resultado)  # True
